    private void botaoBackupActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botaoBackupActionPerformed
    {//GEN-HEADEREND:event_botaoBackupActionPerformed
		this.progresso.setIndeterminate(true);
		
		this.control	= new ControladorBackup();
		
		Boolean backup = this.control.restauraBackup(this.textoDiretorio.getText());
		
		this.progresso.setIndeterminate(false);

		//Backup Restaurado
		if (backup)
		{
			this.janelaSucessoRestaurarBackup = new SucessoRestaurarBackup(this, true);
			this.janelaSucessoRestaurarBackup.setVisible(true);
		}
		//Erro ao restaurar
		else
		{
			this.janelaErroRestaurarBackup = new ErroRestaurarBackup(this, true);
			this.janelaErroRestaurarBackup.setVisible(true);
		}

		this.control = null;
    }//GEN-LAST:event_botaoBackupActionPerformed

    private void botaoProcurarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botaoProcurarActionPerformed
    {//GEN-HEADEREND:event_botaoProcurarActionPerformed
		FileNameExtensionFilter sqlfilter = new FileNameExtensionFilter	("SQL Files (*.sql)", "sql");
            
		
		this.janelaProcura.setVisible(true);  
		this.janelaProcura.setFileFilter(sqlfilter);
		
		int result = this.janelaProcura.showOpenDialog(null); 
		
		if(result == JFileChooser.OPEN_DIALOG)
			this.textoDiretorio.setText(this.janelaProcura.getSelectedFile().toString());
    }//GEN-LAST:event_botaoProcurarActionPerformed



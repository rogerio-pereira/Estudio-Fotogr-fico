    private void botaoCadastrarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botaoCadastrarActionPerformed
    {//GEN-HEADEREND:event_botaoCadastrarActionPerformed
		controladorBD = new ControladorBancodeDados();
		controladorBD.tiposEquipamentos = new TiposEquipamentos();
		controladorBD.tiposEquipamentos.setTipo(this.textoNome.getText().toUpperCase());
		controladorBD.cadastrar(controladorBD.tiposEquipamentos, (JFrame)this);
		controlador = null;

		this.textoNome.setText("");
		this.getTipoEquipamentos();
    }//GEN-LAST:event_botaoCadastrarActionPerformed

    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botaoExcluirActionPerformed
    {//GEN-HEADEREND:event_botaoExcluirActionPerformed
		controlador		= new ControladorEquipamentos();
		controladorBD	= new ControladorBancodeDados();
		
		String marca	= tabelaTipoEquipamentos.getValueAt(tabelaTipoEquipamentos.getSelectedRow(), 0).toString();
		Long   codigo   = controlador.getCodigoByTipo(marca);
		
		controladorBD.tiposEquipamentos = new TiposEquipamentos();
		controladorBD.apagar(codigo, controladorBD.tiposEquipamentos, (JFrame)this);
		
		controladorBD	= null;
		controlador		= null;
		this.getTipoEquipamentos();
    }//GEN-LAST:event_botaoExcluirActionPerformed


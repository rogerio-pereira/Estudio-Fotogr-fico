    public void keyPressed(KeyEvent e) 
    {
        //TECLA ENTER
        if(e.getKeyCode() == KeyEvent.VK_ENTER)
        {
            botaoCadastrar.doClick();
        }
        
        //TECLA ESC
        if(e.getKeyCode() == KeyEvent.VK_ESCAPE)
        {
            dispose();
        }
    }
	
	/*
	 * Método recuperaMarcas
	 * Recupera todas as marcas no banco de dados
	 */
	private void getTipoEquipamentos()
	{
		controlador = new estudiofotografico.control.ControladorEquipamentos();
		List<String> marcas = controlador.getTipoEquipamentos();
		
		DefaultTableModel modelo = (DefaultTableModel) tabelaTipoEquipamentos.getModel();
		
		//Limpa tabela
		while(tabelaTipoEquipamentos.getRowCount() > 0)
			modelo.removeRow(0);
		
		//Insere Novamente
		for(String marca : marcas)
		{
			modelo.addRow(new String[] {marca.toString()});
		}
		controlador = null;
	}


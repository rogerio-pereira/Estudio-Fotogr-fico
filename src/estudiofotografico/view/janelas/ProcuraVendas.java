	/*
	 * Método setTamanhoTabela
	 * Define o tamanho das colunas da tabela
	 */
	private void configuraTabela()
	{
		DefaultTableCellRenderer alinhamento = new DefaultTableCellRenderer();
		alinhamento.setHorizontalAlignment(SwingConstants.LEFT);
		tabelaVendas.getColumnModel().getColumn(0).setCellRenderer(alinhamento);
		
		tabelaVendas.getColumnModel().getColumn(0).setPreferredWidth(70); 
		tabelaVendas.getColumnModel().getColumn(1).setPreferredWidth(150); 
		tabelaVendas.getColumnModel().getColumn(2).setPreferredWidth(100); 
		tabelaVendas.getColumnModel().getColumn(3).setPreferredWidth(72); 
		tabelaVendas.getColumnModel().getColumn(4).setPreferredWidth(58);
		tabelaVendas.getColumnModel().getColumn(5).setPreferredWidth(150); 
	}
    /*
	 * Método apagaTabela
	 * Apaga a tabela
	 */
	public void apagaTabela()
	{
		DefaultTableModel modelo = (DefaultTableModel) tabelaVendas.getModel();
		while(tabelaVendas.getRowCount() > 0)
			modelo.removeRow(0);
	}
	
	/*
	 * Método insereDados
	 * insere os dados da busca na tabela
	 */
	public void insereDados(List<Vendas> lista)
	{
		DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
		
		for(Vendas venda: lista)
		{
			
			DefaultTableModel modelo = (DefaultTableModel) this.tabelaVendas.getModel();
			modelo.addRow(new String[]	{
											venda.getCodigo().toString(),
											venda.getCliente().getNome(),
											venda.getTipoEvento().getTipo(),
											df.format(venda.getDia().getTime()),
											venda.getHora(),
											venda.getLocalEvento().getNome()
										});
		}
	}
	
	/*
	 * Método setClienteByCod
	 * Busca o Cliente pelo codigo
	 */
	public void setClienteByCod (Long codigo)
	{
		this.controlClientes	= new ControladorClientes();
		Clientes cliente		= this.controlClientes.getClientesByCodigo(codigo);
		this.textoCliente.setText(cliente.getCodigo()+" - "+cliente.getNome());
		this.controlClientes = null;
	}
	
	/*
	 * Método setTipoByCod
	 * Busca o Local do Evento pelo codigo
	 */
	public void setTipoByCod (Long codigo)
	{
		this.controlEventos	= new ControladorEventos();
		TiposEventos tipos	= this.controlEventos.getTiposEventosByCod(codigo);
		this.textoTipoEvento.setText(tipos.getCodigo()+" - "+tipos.getTipo());
		this.controlClientes = null;
	}
	
	/*
	 * Método setLocalByCod
	 * Busca o Local do Evento pelo codigo
	 */
	public void setLocalByCod (Long codigo)
	{
		this.controlEventos	= new ControladorEventos();
		LocaisEventos local	= this.controlEventos.getLocalByCodigo(codigo);
		this.textoLocalEvento.setText(local.getCodigo()+" - "+local.getNome());
		this.controlClientes = null;
	}


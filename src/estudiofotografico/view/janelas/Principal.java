	/*
	 * Método verificaEmpresa
	 * Obtem se tem empresa cadastrada
	 */
	private void verificaEmpresa()
	{
		this.controlEmpresa = new ControladorEmpresa();
		if(!this.controlEmpresa.countEmpresa())
		{
			this.janelaEmpresaNaoCadastrada = new ErroEmpresaNaoCadastrada(this, true);
			this.janelaEmpresaNaoCadastrada.setVisible(true);
			if(this.janelaEmpresaNaoCadastrada.getReturnStatus() == RET_OK)
			{
				this.janelaDadosProprietario = new DadosEmpresa();
				this.janelaDadosProprietario.setVisible(true);
			}
		}
	}
	
	/*
	 * Método verificaUsuario
	 * Verifica o usuario e oculta opções dos menus se nao for administrador
	 */
	private void verificaUsuario()
	{
		if(!this.estudio.user.getNivel())
		{
			this.menuCadastroEquipamentos.setVisible(false);
			this.menuCadastroProdutos.setVisible(false);
			this.menuCadastroServicos.setVisible(false);
			this.menuCadastroEventos.setVisible(false);
			this.menuRelatorios.setVisible(false);
			this.botaoProdutos.setVisible(false);
			this.botaoServicos.setVisible(false);
			this.menuSistemaBackup.setVisible(false);
			this.itemSistemaUsuarioCadastrar.setVisible(false);
			this.itemSistemaProprietario.setVisible(false);
		}
	}
    
